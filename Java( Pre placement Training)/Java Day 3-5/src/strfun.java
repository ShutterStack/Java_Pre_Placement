public class strfun {
    public static void main(String[] args) {
        String s1 = "Hello";
        System.out.println(s1.startsWith("H"));
        System.out.println(s1.startsWith("Hel"));
        System.out.println(s1.startsWith("ello"));
        System.out.println(s1.startsWith("lo"));
    }
}
